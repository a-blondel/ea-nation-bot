    /**
     * Get team by ID, returns null if not found
     */
    public static NhlTeam getByTeamId(int teamId) {
        for (NhlTeam team : values()) {
            if (team.teamId == teamId) {
                return team;
            }
        }
        return null;
    }

    /**
     * Get team name by ID with fallback to "Unknown Team"
     */
    public static String getTeamNameById(int teamId) {
        NhlTeam team = getByTeamId(teamId);
        return team != null ? team.getTeamName() : "Unknown Team";
    }

    /**
     * Get logo path by ID with fallback to default
     */
    public static String getLogoPathById(int teamId) {
        NhlTeam team = getByTeamId(teamId);
        return team != null ? team.getLogoPath() : "TeamLogos/Default.png";
    }

    /**
     * Get background path by venue ID with fallback to default
     */
    public static String getBackgroundPathByVenueId(int venueId) {
        NhlTeam team = getByTeamId(venueId);
        return team != null ? team.getBackgroundPath() : "Backgrounds/Default.jpg";
    }

